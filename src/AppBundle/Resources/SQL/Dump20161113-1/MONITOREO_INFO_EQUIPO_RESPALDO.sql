DROP TABLE IF EXISTS `INFO_EQUIPO_RESPALDO`;

CREATE TABLE `MONITOREO`.`INFO_EQUIPO_RESPALDO` (
  `ID_RESPALDO` INT NOT NULL AUTO_INCREMENT,
  `EQUIPO_ID` INT NOT NULL,
  `DESCRIPCION` VARCHAR(500) NOT NULL,
  `RESPALDO` BLOB NOT NULL,
  `USR_CREACION` VARCHAR(45) NOT NULL,
  `FE_CREACION` DATETIME NOT NULL,
  PRIMARY KEY (`ID_RESPALDO`),
  UNIQUE KEY `ID_RESPALDO_UNIQUE` (`ID_RESPALDO`),
  KEY `EQUIPO_ID_idx` (`EQUIPO_ID`),
  CONSTRAINT `EQUIPO_ID_FK`	
    FOREIGN KEY (`EQUIPO_ID`)
    REFERENCES `MONITOREO`.`INFO_EQUIPO` (`ID_EQUIPO`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION);
